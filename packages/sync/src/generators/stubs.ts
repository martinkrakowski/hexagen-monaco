import path from 'node:path';
import fs from 'node:fs/promises';
import { SyncConfig } from '../config.js';
import { createEmptyResult, type GeneratorResult } from '../results.js';

/**
 * Generates stub files for ports declared in manifest.module.ports.out.
 * Non-destructive: skips existing files unless --force.
 */
export async function generateStubs(
  modulePath: string,
  config: SyncConfig
): Promise<GeneratorResult> {
  const result = createEmptyResult();

  const stubsEnabled =
    (config as any).manifest?.generator?.sync?.stubs?.enabled !== false;
  if (!stubsEnabled) {
    result.summary = `Stubs disabled`;
    return result;
  }

  const moduleName = path.basename(modulePath);
  const moduleConfig =
    (config as any).manifest?.modules?.find(
      (m: any) => m.name === moduleName
    ) || {};
  const ports = moduleConfig.ports?.out || [];

  const stubsDir = path.join(modulePath, 'src', 'application', 'ports', 'in');

  const dirResult = await ensureDirectory(
    stubsDir,
    config.dryRun,
    config.force
  );
  result.created.push(...(dirResult.created ?? []));
  result.skipped.push(...(dirResult.skipped ?? []));
  result.updated.push(...(dirResult.updated ?? []));
  result.dryRunOperations += dirResult.dryRunOperations ?? 0;

  for (const portName of ports) {
    const portPath = path.join(stubsDir, `${portName.toLowerCase()}.port.ts`);
    const content = generatePortStub(portName);

    const fileResult = await writeStubFile(
      portPath,
      content,
      config.dryRun,
      config.force
    );
    result.created.push(...(fileResult.created ?? []));
    result.skipped.push(...(fileResult.skipped ?? []));
    result.updated.push(...(fileResult.updated ?? []));
    result.dryRunOperations += fileResult.dryRunOperations ?? 0;
  }

  result.summary = `Stubs generated for ${moduleName}`;
  return result;
}

function generatePortStub(portName: string): string {
  return `// @generated by @hexagen/sync
export interface I${portName}Port {
  execute(input: any): Promise<any>;
}
`;
}

async function ensureDirectory(
  dirPath: string,
  dryRun: boolean,
  force: boolean
): Promise<Partial<GeneratorResult>> {
  const r: Partial<GeneratorResult> = {
    created: [],
    skipped: [],
    updated: [],
    dryRunOperations: 0,
  };
  if (dryRun) {
    r.created = [dirPath];
    r.dryRunOperations = 1;
    return r;
  }
  try {
    await fs.mkdir(dirPath, { recursive: true });
    r.created = [dirPath];
    return r;
  } catch (err: any) {
    if (err.code === 'EEXIST') {
      r.skipped = [dirPath];
      return r;
    }
    r.error = err;
    return r;
  }
}

async function writeStubFile(
  filePath: string,
  content: string,
  dryRun: boolean,
  force: boolean
): Promise<Partial<GeneratorResult>> {
  const r: Partial<GeneratorResult> = {
    created: [],
    skipped: [],
    updated: [],
    dryRunOperations: 0,
  };
  if (dryRun) {
    r.created = [filePath];
    r.dryRunOperations = 1;
    return r;
  }

  // Non-destructive: skip if file already exists (unless --force)
  try {
    await fs.access(filePath);
    r.skipped = [filePath];
    return r;
  } catch {}

  try {
    await fs.writeFile(filePath, content, 'utf8');
    r.created = [filePath];
    return r;
  } catch (err: any) {
    r.error = err;
    return r;
  }
}
