// packages/sync/src/generators/barrels.ts

import path from 'node:path';
import fs from 'node:fs/promises';
import { SyncConfig } from '../config.js';
import { createEmptyResult, type GeneratorResult } from '../results.js';

/**
 * Generates barrel files (index.ts) for layers where enabled in manifest.
 * Returns structured GeneratorResult instead of void.
 * Data-driven from manifest.generator.sync.barrels (boolean or {enabled}).
 */
export async function generateBarrels(
  modulePath: string,
  config: SyncConfig
): Promise<GeneratorResult> {
  const result = createEmptyResult();

  // Defensive access (SyncConfig type does not declare .manifest)
  const manifest = (config as any).manifest;
  const barrelsConfig = manifest?.generator?.sync?.barrels;

  const enabled =
    typeof barrelsConfig === 'boolean'
      ? barrelsConfig
      : !!barrelsConfig?.enabled;

  if (!enabled) {
    result.summary = `Barrels disabled for module ${modulePath}`;
    return result;
  }

  const layers = Object.keys(manifest?.generator?.sync?.layers ?? {});

  for (const layer of layers) {
    const barrelPath = path.join(modulePath, 'src', layer, 'index.ts');

    const content = generateBarrelContent(layer);

    const barrelResult = await writeBarrelFile(
      barrelPath,
      content,
      config.dryRun,
      config.force
    );

    result.created.push(...(barrelResult.created ?? []));
    result.skipped.push(...(barrelResult.skipped ?? []));
    result.updated.push(...(barrelResult.updated ?? []));
    result.dryRunOperations += barrelResult.dryRunOperations ?? 0;
  }

  result.summary = `Barrel files generated (${result.created.length} created, ${result.skipped.length} skipped)`;
  return result;
}

/** Simple barrel content (extendable later with real exports) */
function generateBarrelContent(layer: string): string {
  return `// @generated by @hexagen/sync
// Barrel file for ${layer} layer — auto-regenerated on yarn sync

export * from './entities/index.js';
export * from './value-objects/index.js';
// TODO: add more re-exports as stubs grow
`;
}

/** Safe barrel file writer */
async function writeBarrelFile(
  filePath: string,
  content: string,
  dryRun: boolean,
  force: boolean
): Promise<Partial<GeneratorResult>> {
  const r: Partial<GeneratorResult> = {
    created: [],
    skipped: [],
    updated: [],
    dryRunOperations: 0,
  };

  if (dryRun) {
    r.created = [filePath];
    r.dryRunOperations = 1;
    return r;
  }

  try {
    await fs.writeFile(filePath, content, 'utf8');
    r.created = [filePath];
    return r;
  } catch (err: any) {
    if (err.code === 'EEXIST' && !force) {
      r.skipped = [filePath];
      return r;
    }
    r.error = err;
    return r;
  }
}
